int	turn_iter_to_col(int iter, int colour_var)
{
	if (colour_var % 3 == 1)
		return ((iter * 52351 % 142) * 255335);
	if (colour_var % 3 == 2)
		return (iter * 25632 % 55 * 666);
	if (iter >= 128)
		iter = 127;
	return (iter * 2 + iter * 2 * 256 + 100 * 256 * 256);
}
